#!/usr/bin/env python3
"""
This module contains the function np_shape that calculates the shape
of a numpy.ndarray
"""


def np_shape(matrix):
    """
    Function that calculates the shape of a numpy.ndarray
    Args:
        matrix (numpy.ndarray): The input array.
    Returns:
        tuple: The shape of the input array.
    """
    return matrix.shape

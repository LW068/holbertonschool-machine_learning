#!/usr/bin/env python3

"""
This module contains a function that concatenates two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Args:
        arr1 (list): A list of ints or floats.
        arr2 (list): A list of ints or floats.

    Returns:
        A new list that is the concatenation of arr1 and arr2.
    """
    return arr1 + arr2
